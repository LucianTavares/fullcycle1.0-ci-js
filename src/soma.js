const somaDoisNumeros = (primeiroNumero, segundoNumero) => {
  return primeiroNumero + segundoNumero;
};

const subtracaoDoisNumeros = (primeiroNumero, segundoNumero) => {
  return primeiroNumero - segundoNumero;
}

const multiplicacaoDoisNumeros = (primeiroNumero, segundoNumero) => {
  return primeiroNumero * segundoNumero;
}

const divisaoDoisNumeros = (primeiroNumero, segundoNumero) => {
  return primeiroNumero / segundoNumero;
}

const potenciaDoisNumeros = (primeiroNumero, segundoNumero) => {
  return primeiroNumero ** segundoNumero;
}

module.exports = { somaDoisNumeros, subtracaoDoisNumeros, multiplicacaoDoisNumeros, divisaoDoisNumeros, potenciaDoisNumeros };
